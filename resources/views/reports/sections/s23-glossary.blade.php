    <h2>Glossary &amp; FAQ</h2>

    <table class="kv">
        <tr><td class="k">Mask Profile</td><td>The public or adapted behavioral style — how a person consciously or unconsciously presents themselves to others.</td></tr>
        <tr><td class="k">Latent Profile</td><td>The instinctive, underlying behavioral style — how a person tends to react under pressure or when not self-monitoring.</td></tr>
        <tr><td class="k">Percentile</td><td>A score of 70 means the participant scored higher than 70% of the norming population on that dimension.</td></tr>
        <tr><td class="k">Shift</td><td>The difference between Latent and Mask percentiles. A large positive shift on a dimension means the natural self exceeds the adapted style on that dimension; negative means the adapted style is expressed more than the natural one.</td></tr>
        <tr><td class="k">Pattern</td><td>The combination of high (above 50th percentile) dimensions. A pattern of 12 (binary 1100) means D and I are both above 50.</td></tr>
        <tr><td class="k">Can DISC change?</td><td>Yes — while core tendencies are relatively stable, context, role, and personal growth can all shift the profile over time.</td></tr>
